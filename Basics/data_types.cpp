#include <iostream>

int main() {
    int number = 10;
    float decimal = 5.5;
    char letter = 'A';
    std::string message = "Hello, C++!";

    std::cout << "Integer: " << number << std::endl;
    std::cout << "Float: " << decimal << std::endl;
    std::cout << "Character: " << letter << std::endl;
    std::cout << "String: " << message << std::endl;

    return 0;
}
